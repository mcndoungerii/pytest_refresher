
class General:

    def upper_case(self,text: str):
        return text.upper()

    def lower_case(self,text: str):
        return text.lower()

    def title_case(self,text: str):
        return text.title()

    def capitalize(self,text: str):
        return text.capitalize()

    def is_title(self,text: str):
        return text.istitle()
